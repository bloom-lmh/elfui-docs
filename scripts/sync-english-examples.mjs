import { readFile, writeFile } from "node:fs/promises";

const config = await readFile(new URL("../.vitepress/config.ts", import.meta.url), "utf8");

const linksFor = (name, locale) => {
  const start = config.indexOf(`const ${name}`);
  const end = config.indexOf("\n];", start);

  if (start === -1 || end === -1) throw new Error(`Cannot read ${name} from the VitePress config.`);

  return [...config.slice(start, end).matchAll(new RegExp(`"(/${locale}/[^"]+)"`, "g"))].map(
    ([, link]) => link
  );
};

const englishLinks = linksFor("englishSidebar", "en");
const chineseLinks = linksFor("zhSidebar", "zh");

if (englishLinks.length !== chineseLinks.length) {
  throw new Error(`Sidebar page counts differ: en=${englishLinks.length}, zh=${chineseLinks.length}.`);
}

const localePathMap = Object.fromEntries([
  ["/en/", "/zh/"],
  ["/zh/", "/en/"],
  ...englishLinks.flatMap((englishLink, index) => [
    [englishLink, chineseLinks[index]],
    [chineseLinks[index], englishLink]
  ])
]);

await writeFile(
  new URL("../.vitepress/theme/locale-paths.generated.ts", import.meta.url),
  `// Generated by scripts/sync-english-examples.mjs. Do not edit manually.\nexport const localePathMap = ${JSON.stringify(localePathMap, null, 2)} as const;\n`
);

const codeFencePattern = /^(`{3,})([^\r\n]*)\r?\n([\s\S]*?)^\1[ \t]*$/gm;

const codeFences = (markdown) => [...markdown.matchAll(codeFencePattern)];
const languageOf = (info) => info.trim().replace(/\{[^}]*\}$/, "");

for (const [index, englishLink] of englishLinks.entries()) {
  const chineseLink = chineseLinks[index];
  const [englishPath, chinesePath] = [englishLink, chineseLink].map((link) =>
    new URL(`..${link}.md`, import.meta.url)
  );
  const [english, chinese] = await Promise.all([readFile(englishPath, "utf8"), readFile(chinesePath, "utf8")]);
  const englishFences = codeFences(english);
  const chineseFences = codeFences(chinese);

  if (englishFences.length !== chineseFences.length) {
    throw new Error(
      `Code fence counts differ for ${englishLink} and ${chineseLink}: en=${englishFences.length}, zh=${chineseFences.length}.`
    );
  }

  let fenceIndex = 0;
  const synchronized = english.replace(codeFencePattern, (block, marker, info, body) => {
    const chineseFence = chineseFences[fenceIndex];
    const chineseInfo = chineseFence[2];
    fenceIndex += 1;

    if (languageOf(info) !== languageOf(chineseInfo)) {
      throw new Error(
        `Code fence languages differ for ${englishLink} and ${chineseLink} at block ${fenceIndex}: en=${languageOf(info)}, zh=${languageOf(chineseInfo)}.`
      );
    }

    return `${marker}${chineseInfo}\n${body}${marker}`;
  });

  if (synchronized !== english) await writeFile(englishPath, synchronized);
}

console.log(`Synchronized navigation paths and code highlights for ${englishLinks.length} English documentation pages.`);
